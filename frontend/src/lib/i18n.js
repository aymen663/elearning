export const useLangStore = create((set, get) => ({
    lang: (typeof window !== 'undefined' && localStorage.getItem('eduai_lang')) || 'fr',
    setLang: (l) => {
        localStorage.setItem('eduai_lang', l);
        set({ lang: l });
    },
    t: (key) => {
        const l = get().lang;
        return translations[l]?.[key] || translations.fr[key] || key;
    },
}));
